/*Javascript Math object is used to perform mathematical operations on numbers. 
All the properties of Math are static and unlike other objects, it does not have
 a constructor. */
let sqrt2= Math.SQRT2;
console.log(sqrt2);

let neg= -22;
console.log(Math.abs(neg));

let ceils= 2.223;
console.log(Math.ceil(ceils));

let floors= 22.33;
console.log(Math.floor(floors));

let randoms= Math.random();
console.log(randoms);

let mini= Math.min(1,2,34,5,3,3,3,4,3,3,33,233);
console.log(mini);
